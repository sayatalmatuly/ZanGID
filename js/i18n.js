  function getValue(obj, path) {
    return String(path || '')
      .split('.')
      .reduce(function (acc, key) {
        return acc && Object.prototype.hasOwnProperty.call(acc, key) ? acc[key] : undefined;
      }, obj);
  }

  function interpolate(value, vars) {
    if (typeof value !== 'string' || !vars) return value;
    return value.replace(/\{\{(.*?)\}\}/g, function (_, key) {
      var trimmed = String(key).trim();
      return Object.prototype.hasOwnProperty.call(vars, trimmed) ? vars[trimmed] : '';
    });
  }

  function cloneValue(value) {
    if (Array.isArray(value)) return value.map(cloneValue);
    if (value && typeof value === 'object') {
      return Object.keys(value).reduce(function (acc, key) {
        acc[key] = cloneValue(value[key]);
        return acc;
      }, {});
    }
    return value;
  }

  function get(path, lang) {
    var language = lang || 'ru';
    var primary = getValue(messages[language] || messages.ru, path);
    if (primary !== undefined) return cloneValue(primary);
    var fallback = getValue(messages.ru, path);
    return fallback === undefined ? undefined : cloneValue(fallback);
  }

  function t(path, vars, lang) {
    var value = get(path, lang);
    if (value === undefined) return path;
    return interpolate(value, vars);
  }

  window.ZanGidI18n = {
    defaultLanguage: 'ru',
    messages: messages,
    get: get,
    t: t
  };
